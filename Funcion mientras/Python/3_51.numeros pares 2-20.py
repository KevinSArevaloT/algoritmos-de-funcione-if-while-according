# Nombre: Números pares del 2 al 20
# Entradas: Ninguna
# Salidas: Muestra en pantalla los números pares del 2 al 20
# Proceso: Se utiliza un ciclo while que incrementa de 2 en 2 desde 2 hasta 20
# Autor: Kevin Arevalo

def numeros_pares_2_20():
    print("=====Números pares del 2 al 20=====")
    
    i = 2
    while i <= 20:
        print(i)
        i += 2
    
    print("=====Fin del programa=====")

# Ejecutar el programa
numeros_pares_2_20()
